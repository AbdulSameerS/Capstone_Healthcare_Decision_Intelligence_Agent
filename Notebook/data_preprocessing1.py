axes[0].plot(lr_fpr, lr_tpr, label=f'Logistic Reg (AUC={roc_auc_score(y_test, lr_proba):.3f})')
axes[0].plot(xgb_fpr, xgb_tpr, label=f'XGBoost (AUC={roc_auc_score(y_test, xgb_proba):.3f})')
