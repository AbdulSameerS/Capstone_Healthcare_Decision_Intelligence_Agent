    prec = tp / (tp + fp) if (tp + fp) > 0 else 0
    rec = tp / (tp + fn) if (tp + fn) > 0 else 0
    f1 = 2 * prec * rec / (prec + rec) if (prec + rec) > 0 else 0
    print(f"Threshold={thresh:.2f}  Precision={prec:.3f}  Recall={rec:.3f}  F1={f1:.3f}")

